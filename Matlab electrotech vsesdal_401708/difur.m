function dydt = difur(t,y)
% Параметры задачи
R1=40;
R2=12;
R3=15;
C1=25e-6;
C2=45e-6;
C3=60e-6;
C4=25e-6;
L1=30e-3;
L2=30e-3;
U=120*sin(120*t);
% Алгебраические уравнения
i3=y(5)+y(6);
i2=(U-y(1)-y(2)-R1*i3)/(R1+R2);
i1=i2+i3;
% Дифференциальные уравнения
dydt(1,1)=i1/C1;
dydt(2,1)=i2/C2;
dydt(3,1)=i3/C3;
dydt(4,1)=y(6)/C4;
dydt(5,1)=(y(2)-y(3)+R2*i2)/L1;
dydt(6,1)=(y(2)-y(3)-y(4)+R2*i2-R3*y(6))/L2;
end

